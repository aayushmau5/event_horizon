  def get_theme("nature"), do: nature()
  def get_theme("rosepine-dark"), do: rosepine_dark()
  def get_theme(_), do: default_dark()

  @doc """
  Returns all themes with their CSS variables for embedding in the page.
  """
  def themes_with_variables do
    Enum.map(@themes, fn theme ->
      Map.put(theme, :variables, get_theme(theme.id))
    end)
  end

